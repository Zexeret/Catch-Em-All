// Our map is basically made up of tiles.
// you can find these numbers in tiled editor > resize map options
const TILED_MAP_WIDTH = 70;
const TILED_MAP_HEIGHT = 40;

export const generateCollisionMap = (initial_offset: Coordinates) => {
  const collisionMap: Array<Array<number>> = [];
  const collisionMapBoundary: Array<Boundary> = [];

  for (let i = 0; i < collisionGrid.length; i += TILED_MAP_WIDTH) {
    collisionMap.push(collisionGrid.slice(i, TILED_MAP_WIDTH + i));
  }

  collisionMap.forEach((row, i) => {
    row.forEach((pixel, j) => {
      if (pixel === 1025) {
        collisionMapBoundary.push(
          new Boundary({
            position: {
              x: j * Boundary.boundaryWidth + initial_offset.x,
              y: i * Boundary.boundaryHeight + initial_offset.y,
            },
            velocity: WALK_VELOCITY,
          })
        );
      }
    });
  });

  return collisionMapBoundary;
};

export const checkCollision = (rect1: Sprite, rect2: BaseSprite) => {
  const checkLeftCollision =
    rect1.position.x <= rect2.position.x + Boundary.boundaryWidth;

  const checkRightCollision =
    rect1.position.x + Boundary.boundaryWidth >= rect2.position.x;

  const checkTopCollision =
    rect1.position.y <= rect2.position.y + Boundary.boundaryHeight;
  const checkDownCollision =
    rect1.position.y + rect1.height >= rect2.position.y;

  return (
    checkLeftCollision &&
    checkRightCollision &&
    checkTopCollision &&
    checkDownCollision
  );
};
