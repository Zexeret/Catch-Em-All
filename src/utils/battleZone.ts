// Our map is basically made up of tiles.
// you can find these numbers in tiled editor > resize map options
const TILED_MAP_WIDTH = 70;
const TILED_MAP_HEIGHT = 40;

export const generateBattleZoneMap = (initial_offset: Coordinates) => {
  const battleZoneMap: Array<Array<number>> = [];
  const battleZoneMapBoundary: Array<Boundary> = [];

  for (let i = 0; i < battleZoneData.length; i += TILED_MAP_WIDTH) {
    battleZoneMap.push(battleZoneData.slice(i, TILED_MAP_WIDTH + i));
  }

  battleZoneMap.forEach((row, i) => {
    row.forEach((pixel, j) => {
      if (pixel === 1025) {
        battleZoneMapBoundary.push(
          new Boundary({
            position: {
              x: j * Boundary.boundaryWidth + initial_offset.x,
              y: i * Boundary.boundaryHeight + initial_offset.y,
            },
            velocity: WALK_VELOCITY,
          })
        );
      }
    });
  });

  return battleZoneMapBoundary;
};
